package ie.atu;

public class Business {
    private String name;
    private long id;
    private String address;
    private int openTime;
    private int closeTime;

    public String getName() {
        return name;
    }

    public long getId() {
        return id;
    }

    public String getAddress() {
        return address;
    }

    public int getOpenTime() {
        return openTime;
    }

    public int getCloseTime() {
        return closeTime;
    }

}
